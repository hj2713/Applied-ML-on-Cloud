"""
plot_results.py

PURPOSE:
This is the final analysis script for the project. It reads all the raw benchmark data 
generated by the different team members, processes it, and generates the final academic-grade 
visualizations and summary tables needed for the final presentation.

INPUTS:
It reads all `.jsonl` files located in the `results/raw/` directory. 
These files contain the per-request metrics (latency, throughput, cost) generated during the sweeps.
It ignores any file containing `_t99` (which are just quality-check samples).

OUTPUTS:
1. results/plots/*.png : Generates 3 multi-panel line graphs per GPU type (Throughput, TTFT, Acceptance Rate).
2. results/tables/*.csv : Generates numerical summary tables (Cost, Crossover points, and Aggregated Means).
"""

import os
import glob
import json
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

def load_data(results_dir):
    records = []
    # Find all jsonl files, exclude the quality check (t99) files
    files = glob.glob(os.path.join(results_dir, "raw", "*.jsonl"))
    for f in files:
        if "_t99.jsonl" in f:
            continue
        with open(f, 'r') as file:
            for line in file:
                if line.strip():
                    records.append(json.loads(line))
    return pd.DataFrame(records)

def main():
    base_dir = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    results_dir = os.path.join(base_dir, "results")
    plots_dir = os.path.join(results_dir, "plots")
    tables_dir = os.path.join(results_dir, "tables")

    os.makedirs(plots_dir, exist_ok=True)
    os.makedirs(tables_dir, exist_ok=True)

    print("Loading data...")
    df = load_data(results_dir)
    if df.empty:
        print("No valid benchmark data found in results/raw/ (excluding _t99 files).")
        return

    print(f"Loaded {len(df)} requests.")
    
    # ---------------------------------------------------------
    # Academic Formatting Setup
    # ---------------------------------------------------------
    sns.set_theme(style="whitegrid", context="talk")
    # Custom color palette for distinction
    palette = {"baseline": "#1f77b4", "eagle3": "#ff7f0e", "mlx_baseline": "#2ca02c", "mlx_spec": "#d62728"}

    gpu_types = df['gpu_type'].unique()
    tasks = df['task'].unique()

    # 1. Generate Academic-Grade Multi-Panel Plots
    for gpu in gpu_types:
        gpu_df = df[df['gpu_type'] == gpu]
        if gpu_df.empty:
            continue
            
        # Ensure we only use colors for systems present in this subset
        current_systems = gpu_df['system'].unique()
        current_palette = {sys: palette.get(sys, "#333333") for sys in current_systems}

        # --- Plot A: Throughput Overview (1x3 Grid) ---
        fig, axes = plt.subplots(1, len(tasks), figsize=(18, 6), sharey=True)
        if len(tasks) == 1:
            axes = [axes]
            
        for i, task in enumerate(tasks):
            task_df = gpu_df[gpu_df['task'] == task]
            sns.lineplot(
                data=task_df, x='concurrency', y='tokens_per_sec', hue='system', 
                marker='o', errorbar=('ci', 95), ax=axes[i], palette=current_palette, linewidth=2.5
            )
            axes[i].set_title(f"Task: {task.capitalize()}", fontweight='bold')
            axes[i].set_xlabel("Concurrency")
            if i == 0:
                axes[i].set_ylabel("Throughput (Tokens / Sec)")
            else:
                axes[i].set_ylabel("")
            axes[i].set_xticks(sorted(task_df['concurrency'].unique()))
            # Only keep legend on the last subplot
            if i < len(tasks) - 1 and axes[i].get_legend():
                axes[i].get_legend().remove()

        plt.suptitle(f"Speculative Decoding Throughput vs. Baseline ({gpu} GPU)", fontsize=20, y=1.05, fontweight='bold')
        plt.tight_layout()
        plt.savefig(os.path.join(plots_dir, f"academic_throughput_overview_{gpu}.png"), dpi=300, bbox_inches='tight')
        plt.close()

        # --- Plot B: Time to First Token Overview (1x3 Grid) ---
        fig, axes = plt.subplots(1, len(tasks), figsize=(18, 6), sharey=True)
        if len(tasks) == 1:
            axes = [axes]
            
        for i, task in enumerate(tasks):
            task_df = gpu_df[gpu_df['task'] == task]
            sns.lineplot(
                data=task_df, x='concurrency', y='ttft_ms', hue='system', 
                marker='s', errorbar=('ci', 95), ax=axes[i], palette=current_palette, linewidth=2.5
            )
            axes[i].set_title(f"Task: {task.capitalize()}", fontweight='bold')
            axes[i].set_xlabel("Concurrency")
            if i == 0:
                axes[i].set_ylabel("TTFT (ms) - Lower is Better")
            else:
                axes[i].set_ylabel("")
            axes[i].set_xticks(sorted(task_df['concurrency'].unique()))
            if i < len(tasks) - 1 and axes[i].get_legend():
                axes[i].get_legend().remove()

        plt.suptitle(f"Time to First Token (TTFT) Impact ({gpu} GPU)", fontsize=20, y=1.05, fontweight='bold')
        plt.tight_layout()
        plt.savefig(os.path.join(plots_dir, f"academic_ttft_overview_{gpu}.png"), dpi=300, bbox_inches='tight')
        plt.close()

        # --- Plot C: Time Per Output Token (TPOT) (1x3 Grid) ---
        fig, axes = plt.subplots(1, len(tasks), figsize=(18, 6), sharey=True)
        if len(tasks) == 1:
            axes = [axes]
            
        for i, task in enumerate(tasks):
            task_df = gpu_df[gpu_df['task'] == task]
            sns.lineplot(
                data=task_df, x='concurrency', y='tpot_ms', hue='system', 
                marker='^', errorbar=('ci', 95), ax=axes[i], palette=current_palette, linewidth=2.5
            )
            axes[i].set_title(f"Task: {task.capitalize()}", fontweight='bold')
            axes[i].set_xlabel("Concurrency")
            if i == 0:
                axes[i].set_ylabel("TPOT (ms) - Lower is Better")
            else:
                axes[i].set_ylabel("")
            axes[i].set_xticks(sorted(task_df['concurrency'].unique()))
            if i < len(tasks) - 1 and axes[i].get_legend():
                axes[i].get_legend().remove()

        plt.suptitle(f"Time Per Output Token (TPOT) ({gpu} GPU)", fontsize=20, y=1.05, fontweight='bold')
        plt.tight_layout()
        plt.savefig(os.path.join(plots_dir, f"academic_tpot_overview_{gpu}.png"), dpi=300, bbox_inches='tight')
        plt.close()

        # --- Plot D: Cost Analysis (Cost per 1M Tokens) (1x3 Grid) ---
        # Calculate cost per 1M tokens
        gpu_df = gpu_df.copy()
        gpu_df['cost_per_1m_tokens'] = (gpu_df['gpu_cost_usd'] / gpu_df['output_tokens']) * 1_000_000

        fig, axes = plt.subplots(1, len(tasks), figsize=(18, 6), sharey=True)
        if len(tasks) == 1:
            axes = [axes]
            
        for i, task in enumerate(tasks):
            task_df = gpu_df[gpu_df['task'] == task]
            sns.barplot(
                data=task_df, x='concurrency', y='cost_per_1m_tokens', hue='system', 
                ax=axes[i], palette=current_palette, errorbar=None, alpha=0.8
            )
            axes[i].set_title(f"Task: {task.capitalize()}", fontweight='bold')
            axes[i].set_xlabel("Concurrency")
            if i == 0:
                axes[i].set_ylabel("Cost per 1M Tokens ($)")
            else:
                axes[i].set_ylabel("")
            axes[i].set_xticks(range(len(sorted(task_df['concurrency'].unique()))))
            if i < len(tasks) - 1 and axes[i].get_legend():
                axes[i].get_legend().remove()

        gpu_rates = {"T4": "$0.35/hr", "L4": "$0.70/hr", "A100": "$3.67/hr", "mac": "$0.00/hr (Local)"}
        rate_str = gpu_rates.get(gpu, "Unknown Rate")
        plt.suptitle(f"Cost Analysis: Price per 1M Output Tokens ({gpu} GPU)", fontsize=20, y=1.08, fontweight='bold')
        fig.text(0.5, 0.98, f"Reference: GCP On-Demand Hourly Rate ({rate_str}) | Calculation: (Hourly Rate / 3600) * Request Latency", ha='center', fontsize=14, fontstyle='italic', color='dimgrey')
        
        plt.tight_layout()
        # Adjust layout to fit the text at the top
        plt.subplots_adjust(top=0.88)
        plt.savefig(os.path.join(plots_dir, f"academic_cost_overview_{gpu}.png"), dpi=300, bbox_inches='tight')
        plt.close()

        # --- Plot E: Acceptance Rate Analysis ---
        spec_df = gpu_df.dropna(subset=['acceptance_rate'])
        if not spec_df.empty:
            plt.figure(figsize=(10, 6))
            sns.lineplot(
                data=spec_df, x='concurrency', y='acceptance_rate', hue='task', 
                marker='D', errorbar=('ci', 95), linewidth=2.5
            )
            plt.title(f"Speculative Acceptance Rate by Domain ({gpu} GPU)", fontsize=18, fontweight='bold')
            plt.xlabel("Concurrency")
            plt.ylabel("Acceptance Rate")
            plt.ylim(0, 1.0)
            plt.xticks(sorted(spec_df['concurrency'].unique()))
            plt.tight_layout()
            plt.savefig(os.path.join(plots_dir, f"academic_acceptance_rate_{gpu}.png"), dpi=300)
            plt.close()

    # ---------------------------------------------------------
    # 2. Generate Summary Tables
    # ---------------------------------------------------------
    print("Generating summary CSVs...")
    summary_df = df.groupby(['gpu_type', 'task', 'system', 'concurrency']).agg({
        'ttft_ms': ['mean', 'std'],
        'tpot_ms': ['mean', 'std'],
        'tokens_per_sec': ['mean', 'std'],
        'total_latency_ms': 'mean',
        'acceptance_rate': 'mean'
    }).reset_index()
    # Flatten multi-level columns for CSV
    summary_df.columns = ['_'.join(col).strip() if col[1] else col[0] for col in summary_df.columns.values]
    summary_df.to_csv(os.path.join(tables_dir, "academic_summary.csv"), index=False)

    # Cost summary
    cost_df = df.groupby(['gpu_type', 'system']).agg(
        total_cost_usd=('gpu_cost_usd', 'sum'),
        total_requests=('request_id', 'count')
    ).reset_index()
    cost_df.to_csv(os.path.join(tables_dir, "cost_summary.csv"), index=False)

    # Calculate crossover points (Tokens/Sec)
    crossover_data = []
    for gpu in gpu_types:
        for task in tasks:
            subset = df[(df['gpu_type'] == gpu) & (df['task'] == task)]
            # Get mean TPS per concurrency
            mean_tps = subset.groupby(['system', 'concurrency'])['tokens_per_sec'].mean().reset_index()
            
            baseline = mean_tps[mean_tps['system'].str.contains('baseline')]
            speculative = mean_tps[mean_tps['system'].str.contains('eagle3|mlx_spec')]
            
            if not baseline.empty and not speculative.empty:
                merged = pd.merge(
                    baseline[['concurrency', 'tokens_per_sec']], 
                    speculative[['concurrency', 'tokens_per_sec', 'system']], 
                    on='concurrency', 
                    suffixes=('_base', '_spec')
                )
                crossovers = merged[merged['tokens_per_sec_base'] > merged['tokens_per_sec_spec']]
                if not crossovers.empty:
                    crossover_concurrency = crossovers['concurrency'].min()
                    crossover_data.append({
                        'gpu_type': gpu, 'task': task,
                        'speculative_system': crossovers.iloc[0]['system'],
                        'crossover_concurrency': crossover_concurrency
                    })
                else:
                    crossover_data.append({
                        'gpu_type': gpu, 'task': task,
                        'speculative_system': speculative.iloc[0]['system'],
                        'crossover_concurrency': '>32 (Never Crossed)'
                    })
                    
    if crossover_data:
        cross_df = pd.DataFrame(crossover_data)
        cross_df.to_csv(os.path.join(tables_dir, "crossover_points.csv"), index=False)

    print(f"Generated academic-grade plots in results/plots/")
    print(f"Generated CSVs in results/tables/")
    print("Done!")

if __name__ == "__main__":
    main()
